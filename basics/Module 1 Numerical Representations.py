# underscores in numbers to improve readability
12000300
12_000_300

# scientific notation
# 3e4 = 3E4 = 3 * 10000 = 30000
# 3e-4 = 3E-4 = 3 * 1/10000 = .0003
print(0.000000000000000000000000000000000003)

# octal numbers
# start with 0o or 0O
# each digit from 0 to 7 only, 8 & 9 not permitted
print(0o123)
print(0O017)

# hexadecimal numbers
# start with 0x or 0X
print(0x123)
